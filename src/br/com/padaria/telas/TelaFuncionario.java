    private void Salvar() {
        String sql = "save int funcionario(cpf, nome, ctps, cargo, salario) values (?, ?, ?, ?, ?)";
        try {

            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtFunCpf.getText());
            pst.setString(2, txtFunNome.getText());
            pst.setString(3, txtFunCtps.getText());
            pst.setString(4, txtFunCargo.getText());
            pst.setString(5, txtFunSal.getText());
            if ((txtFunCpf.getText().isEmpty()) || (txtFunNome.getText().isEmpty()) || ((txtFunCtps.getText().isEmpty()))) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatorios");

            }

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
    }

