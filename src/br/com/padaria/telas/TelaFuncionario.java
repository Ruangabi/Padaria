    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        FuncionarioDAO dao = new FuncionarioDAO();
        funcionarios = new ArrayList();
        this.modelo.setNumRows(0);
        try {
            funcionarios = (ArrayList<Funcionario>) dao.Pesquisa(TxtPesquisa.getText());
            for (Funcionario f : funcionarios) {
                this.modelo.addRow(new Object[]{f.getNome(), f.getCpf_fun(), f.getCarteira_trabalho(), f.getCargo(), f.getSalario_R$()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void getall() {

        FuncionarioDAO dao = new FuncionarioDAO();
        funcionarios = new ArrayList<>();
        try {
            funcionarios = (ArrayList<Funcionario>) dao.Select();
            for (Funcionario f : funcionarios) {
                this.modelo.addRow(new Object[]{f.getNome(), f.getCpf_fun(), f.getCarteira_trabalho(), f.getCargo(), f.getSalario_R$()});
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

