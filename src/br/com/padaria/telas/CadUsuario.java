    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        this.modelo.setNumRows(0);
        UsuarioDAO dao = new UsuarioDAO();
        usuarios = new ArrayList();
        try {
            usuarios = dao.Pesquisar(txtPesquisa.getText());
            for (Usuario u : usuarios) {

                this.modelo.addRow(new Object[]{u.getIduser(), u.getFone(), u.getLogin(), u.getSenha(), u.getPerfil()});
            }
        } catch (Exception e) {
            System.out.println("Erro..." + e.getMessage());

        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void resetForm() {
        txtId.setText("");
        txtFone.setText("");
        txtUsu.setText("");
        txtSenha.setText("");
        txtPerfil.setSelectedItem("");
    }
    
     private void getUsuarios() {
        this.modelo.setNumRows(0);
        UsuarioDAO dao = new UsuarioDAO();
        usuarios = new ArrayList();
        try {
            usuarios = dao.Select();
            for (Usuario u : usuarios) {
                this.modelo.addRow(new Object[]{u.getIduser(), u.getFone(), u.getLogin(), u.getSenha(), u.getPerfil()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar usuário: " + e.getMessage());
        }
    }
     
     private Usuario getUsuarioById(int iduser) {
        Usuario u = new Usuario();
        UsuarioDAO dao = new UsuarioDAO();
        try {
            u = dao.SelectOne(iduser);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar usuário: " + e.getMessage());
        }
        return u;
    }
    
    private void onCreate(Usuario usuario) {
        UsuarioDAO dao = new UsuarioDAO();
        try {
            dao.Insert(usuario);
            JOptionPane.showMessageDialog(null, "Usuário salvo com sucesso!");
            this.resetForm();
            this.getUsuarios();
        } catch (Exception e) {
            System.out.println("Erro: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao inserir usuário!");
        }
    }
    
    private void onUpdate(Usuario usuario) {
        try {
            UsuarioDAO dao = new UsuarioDAO();
            dao.Update(usuario);
            JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso!");
            this.resetForm();
            this.getUsuarios();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar: " + e.getMessage());
        }
    }
    
    
